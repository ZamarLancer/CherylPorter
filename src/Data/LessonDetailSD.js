export const uuidGenerator = () => {
  const S4 = function () {
    return (((1 + Math.random()) * 0x10000) | 0).toString(16).substring(1);
  };
  return (
    S4() +
    S4() +
    '-' +
    S4() +
    '-' +
    S4() +
    '-' +
    S4() +
    '-' +
    S4() +
    S4() +
    S4()
  );
}; //end of uuidGenerator

const GetLessonDetailSD = (lessonID, keyID, couseName) => {
  const data = DATA[getCourseName(couseName)][getKeyName(keyID)].filter(
    i => i.LessonId === lessonID,
  );
  const newData = data.map(i => {
    const minutes = Math.floor(i.Duration / 60);
    const seconds = i.Duration - minutes * 60;
    return {
      ...i,
      id: uuidGenerator(),
      time: `${`${minutes}`.padStart(2, '0')}:${`${seconds}`.padStart(2, '0')}`,
      audioUrl: i.Url,
    };
  });
  return newData;
};

const getCourseName = couseName => {
  // FYV
  // HTHN
  // BTB
  if (couseName === 'FYV') {
    return 'freeYourVoice';
  } else if (couseName === 'HTHN') {
    return 'hitThoseHighNotes';
  } else if (couseName === 'BTB') {
    return 'boomTheBig10';
  } else if (couseName === 'TMVW') {
    return 'tenMinVocalWorkout';
  } else {
    //BTB
    return 'boomTheBig10';
  }
};

export const getKeyName = keyID => {
  if (keyID === 1) {
    return 'standard';
  } else if (keyID === 2) {
    return 'high';
  } else {
    return 'low';
  }
};

