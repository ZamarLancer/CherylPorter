const StartDownload = async arr => {
  return new Promise(async resolve => {
    for await (const element of arr) {
      await DownloadFile(element.Url);
    }
    console.log('herre');
    resolve();
  });
};

const DownloadFile = async url => {
  const isExist = await Download.isFileDownloaded(url);
  if (isExist !== false) {
    console.log('ALREADY DOWNLOADED PATH ', isExist);
    return;
  }
  await Download.DOWNLOAD(
    url,
    () => {},
    () => {},
  );
};

